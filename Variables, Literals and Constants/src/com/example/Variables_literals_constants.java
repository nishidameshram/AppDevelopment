package com.example;

public class Variables_literals_constants
{
    public static void main(String[] args) {
        final int firstnumbr;
        int secnnumbr;

        firstnumbr=13;
        secnnumbr= 20;

        double average;
        average= (firstnumbr + secnnumbr) /2.0;
        System.out.println("Average=" + average);

        String name;
        String surname;
        name="Nishida";
        surname="Meshram";
        System.out.println(name+ " " + surname);
        boolean value;
        value =false;

        System.out.println(value);

        char letter;
        letter = 'j';
        System.out.println(letter);

    }
}
